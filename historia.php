El proyecto busca atender en educación formal a los deportista de alto rendimiento, que sueñan con ser campeones sin que tengan que abandonar su educación formal, mediada por una plataforma tecnológica y unos maestros orientadores del conocimiento, que permitan cumplir el sueño a estos niños, niñas y jóvenes que acompañados por sus familias, no solo son exitosos en su pasión deportiva, sino en los aspectos académicos y cognitivos, siendo ejemplo para la familia y la sociedad, como los resultados que efectivamente se han tenido y consolidado.</p>
                       
